// JavaScript for... of Loop

// for...of with Arrays
// The for..of loop can be used to iterate over an array. For example,

// array
const students = ['John', 'Sara', 'Jack'];

// using for...of
for ( let element of students ) {

    // display the values
    console.log(element);
}

// Output

// John
// Sara
// Jack

// for...of with Strings
// The for...of loop can also be used to iterate over a string. For example,
// string
const language = 'JavaScript';
// using for...of
for ( let char of language ) {  
    // display the characters
    console.log(char);
}
// Output

// J    
// a
// v
// a
// S
// c
// r
// i
// p
// t


